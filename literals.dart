/* Literals
Literals are the only information which is yet not stored in any variable
and written standalone in somewhere in code
*/

void main() {
  //literals

  "name";
  1;
  1.1;

  // assign literals to variables

  var name = "name";
  var age = 1;
  var height = 1.1;

  // print literals
  print(name);
  print(age);
  print(height);
}
